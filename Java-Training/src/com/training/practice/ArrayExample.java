package com.training.practice;

public class ArrayExample {
	
	public static void main(String[] args) {
		//Declaring the Array

		int[] arr = new int[3];
		arr[0] =10;
		arr[1] = 20;
		arr[2] = 30;
		
		//Display Array Elements
		System.out.println("Array Element at Idnex 0 " + arr[0]);
		System.out.println("Array Element at Idnex 1 " + arr[1]);
		System.out.println("Array Element at Idnex 2 " + arr[2]);
	}

}
