package com.java.controlIf;

public class StudentIfElseIf {
	public static void main(String[] args) {  
		String city = "Newark";  
		
		if(city == "SanJose") {  
			System.out.println("city is SanJose");  
		}else if (city == "Columbus") {  
			System.out.println("city is Columbus");  
		}else if(city == "Pheonix") {  
			System.out.println("city is Pheonix");  
		}else {  
			System.out.println(city);  
		}  
	}  
}
